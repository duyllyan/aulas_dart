void exercicioBool() {
  int numero1 = 10;
  int numero2 = 13;

  bool igualdade = numero1 == numero2;
  print("O número1 é igual ao número2: $igualdade");

  bool maiorQue = numero1 > numero2;
  print("O número1 é maior que o número2: $maiorQue");

  bool menorQue = numero1 < numero2;
  print("O número1 é menor que o número2: $menorQue");
}